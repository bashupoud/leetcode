package test;


public class Calculate {
    //method
    public int add(int num1, int num2) {
        int result = num1 + num2;
        return result;
    }
    //Substract
    public int substract(int num1,int num2){
        int result = num1 - num2;
        return result;
    }
    //multiplication
    public double multiply(int num1, int num2){
      int result = num1 * num2;
      return result;
    }
    //div
    public double division(int num1, int num2){
        int result = num1 / num2;
        return result;
    }
}
